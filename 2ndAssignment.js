// #2 Javascript:Strings, Arrays and Functions

// #Q1 Implement any 15 string methods on the String “The Phoenix Guild is the best Women in Web3 Community”
let  sTr= "The Phoenix Guild is the best Women in Web3 Community";

// 1. toUpperCase()
console.log(sTr.toUpperCase());

// 2. toLowerCase()
console.log(sTr.toLowerCase());

// 3. length
console.log(sTr.length);

// 4. indexOf()
console.log(sTr.indexOf("best"));

// 5. lastIndexOf()
console.log(sTr.lastIndexOf("the"));
// 6. includes()
console.log(sTr.includes("Women"));

// 7. startsWith()
console.log(sTr.startsWith("The"));

// 8. endsWith()
console.log(sTr.endsWith("Community"));

// 9. charAt()
console.log(sTr.charAt(0));

// 10. substring()
console.log(sTr.substring(4, 11));

// 11. slice()
console.log(sTr.slice(4, 11));

// 12. split()
console.log(sTr.split(" "));

// 13. replace()
console.log(sTr.replace("Phoenix", "Dragon"));

// 14. concat()
console.log(sTr.concat(" Welcome!"));

// 15. trim()
console.log(sTr.trim());



